#include <stdio.h>
#include "main.h"

/**
 * sum_multiple_of_three_or_five(int n) - Entry point
 *
 * Return: 0 always
 */

int sum_multiple_of_three_or_five(void);
{
	int num, result;

	result = 0;
	for (num == 1; num < 1024; num++)
	{
		if (n % 3 == 0 || n % 5 == 0)
		{
			result = result + num;
		}
	}
	printf('%d', result);
}
